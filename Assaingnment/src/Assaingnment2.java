
public class Assaingnment2 {
	// Write an algoritham to accept a number between 1 to 7 and display corresponding week day.
	// Note : Consider sunday as the first day of week
	public static void main(String[] args) {
		int day=7;
		switch(day)
		{
		case 1:
			System.out.println("Sunday");
			break;
		case 2:
			System.out.println("Monday");
			break;
		case 3:
			System.out.println("Tuesday");
			break;
		case 4:
			System.out.println("Wednesday");
			break;
		case 5:
			System.out.println("Thursday");
			break;
		case 6:
			System.out.println("Friday");
			break;
		case 7:
			System.out.println("Saturday");
			break;
		
		}

	}

}
